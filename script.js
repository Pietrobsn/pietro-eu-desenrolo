const header = document.querySelector("[data-header]");
const menuButton = document.querySelector("[data-menu-button]");
const nav = document.querySelector("[data-nav]");
const navLinks = [...document.querySelectorAll(".site-nav a[href^='#']")];
const year = document.querySelector("[data-year]");
const form = document.querySelector("[data-whatsapp-form]");
const mobileCta = document.querySelector("[data-mobile-cta]");
const languageButtons = [...document.querySelectorAll("[data-language-option]")];

let currentLanguage = "pt";

const getStoredLanguage = () => {
  try {
    const stored = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return stored === "it" || stored === "pt" ? stored : "pt";
  } catch {
    return "pt";
  }
};

const storeLanguage = (language) => {
  try {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
  } catch {
    // localStorage can be blocked in private or restricted browser contexts.
  }
};

const getTranslation = (key) => translations[currentLanguage][key] ?? translations.pt[key] ?? "";

const whatsappUrl = (message) => `https://wa.me/${WHATSAPP_PHONE}?text=${encodeURIComponent(message)}`;

const syncMenuButtonLabel = () => {
  if (!menuButton) return;
  const isOpen = menuButton.getAttribute("aria-expanded") === "true";
  menuButton.setAttribute("aria-label", getTranslation(isOpen ? "menuClose" : "menuOpen"));
};

const closeMenu = () => {
  if (!header || !menuButton || !nav) return;

  nav.classList.remove("is-open");
  header.classList.remove("menu-open");
  menuButton.setAttribute("aria-expanded", "false");
  syncMenuButtonLabel();
};

const applyLanguage = (language, { persist = true } = {}) => {
  currentLanguage = translations[language] ? language : "pt";
  const dictionary = translations[currentLanguage];

  document.documentElement.lang = currentLanguage === "it" ? "it" : "pt-BR";
  document.documentElement.dataset.language = currentLanguage;
  document.title = dictionary.metaTitle;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) element.textContent = dictionary[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (dictionary[key]) element.setAttribute("placeholder", dictionary[key]);
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    if (dictionary[key]) element.setAttribute("aria-label", dictionary[key]);
  });

  document.querySelectorAll("[data-i18n-content]").forEach((element) => {
    const key = element.dataset.i18nContent;
    if (dictionary[key]) element.setAttribute("content", dictionary[key]);
  });

  languageButtons.forEach((button) => {
    const isCurrent = button.dataset.languageOption === currentLanguage;
    button.classList.toggle("is-active", isCurrent);
    button.setAttribute("aria-pressed", String(isCurrent));
  });

  document.querySelectorAll("[data-whatsapp-link]").forEach((link) => {
    const messageKey = link.dataset.whatsappLink === "rent" ? "whatsappRentMessage" : "whatsappMessage";
    link.href = whatsappUrl(dictionary[messageKey]);
  });

  syncMenuButtonLabel();
  if (persist) storeLanguage(currentLanguage);
};

const updateHeader = () => {
  if (!header) return;
  header.classList.toggle("is-scrolled", window.scrollY > 20);
  mobileCta?.classList.toggle("is-visible", window.scrollY > 320);
};

if (menuButton && nav && header) {
  menuButton.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("is-open");
    header.classList.toggle("menu-open", isOpen);
    menuButton.setAttribute("aria-expanded", String(isOpen));
    syncMenuButtonLabel();
  });

  nav.addEventListener("click", (event) => {
    if (event.target.matches("a")) closeMenu();
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") closeMenu();
  });
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.languageOption || "pt");
  });
});

if ("IntersectionObserver" in window && navLinks.length) {
  const sections = navLinks
    .map((link) => document.querySelector(link.getAttribute("href")))
    .filter(Boolean);

  const setActiveLink = (id) => {
    navLinks.forEach((link) => {
      link.classList.toggle("is-active", link.getAttribute("href") === `#${id}`);
    });
  };

  const sectionObserver = new IntersectionObserver(
    (entries) => {
      const visible = entries
        .filter((entry) => entry.isIntersecting)
        .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];

      if (visible) setActiveLink(visible.target.id);
    },
    {
      rootMargin: "-24% 0px -58% 0px",
      threshold: [0.12, 0.35, 0.6],
    },
  );

  sections.forEach((section) => sectionObserver.observe(section));
}

window.addEventListener("scroll", updateHeader, { passive: true });
window.addEventListener("resize", closeMenu);
updateHeader();

if (year) {
  year.textContent = new Date().getFullYear();
}

if (form) {
  form.addEventListener("submit", (event) => {
    event.preventDefault();

    const dictionary = translations[currentLanguage];
    const data = new FormData(form);
    const nome = data.get("nome")?.toString().trim();
    const servico = form.elements.servico?.selectedOptions?.[0]?.textContent.trim();
    const mensagem = data.get("mensagem")?.toString().trim();

    const linhas = [
      dictionary.formWhatsappIntro,
      nome ? `${dictionary.formWhatsappName}: ${nome}` : "",
      servico ? `${dictionary.formWhatsappService}: ${servico}` : "",
      mensagem ? `${dictionary.formWhatsappMessage}: ${mensagem}` : "",
    ].filter(Boolean);

    window.open(whatsappUrl(linhas.join("\n")), "_blank", "noopener");
  });
}

applyLanguage(getStoredLanguage(), { persist: false });
